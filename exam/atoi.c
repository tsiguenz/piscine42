#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int res;

	res = 0;
	i = 0;
	while (str[i])
	{
		res = res * 10 + str[i] - 48;
		i++;
	}
	return (res);
}

int	main()
{
	printf("%d\n", ft_atoi("42"));
}
